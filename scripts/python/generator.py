"""
generator.py
Converts parsed metadata objects into Claude-ready Markdown skill files.
Port of scripts/node/generator.js.
"""

from datetime import date


def generate_skill(metadata_type: str, parsed: dict, config: dict) -> str:
    """
    Generate a Markdown skill file from parsed metadata.

    Args:
        metadata_type: e.g. 'objects', 'flows', 'classes'
        parsed: output from the relevant parser
        config: org-config.json contents as dict

    Returns:
        Markdown string
    """
    builders = {
        'objects':        _build_objects_skill,
        'flows':          _build_flows_skill,
        'classes':        _build_apex_skill,
        'triggers':       _build_apex_skill,
        'lwc':            _build_lwc_skill,
        'permissionsets': _build_permset_skill,
        'profiles':       _build_profile_skill,
        'layouts':        _build_layout_skill,
        'emailTemplates': _build_template_skill,
    }

    builder = builders.get(metadata_type)
    if not builder:
        return f'# {metadata_type}\n\nNo skill builder registered for this type.\n'

    return builder(parsed, config)


